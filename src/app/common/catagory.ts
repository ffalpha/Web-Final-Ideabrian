export class selector  {
  
  static select(aa) {
      if(aa==1){
         return "Agriculture and cropes"; 

      }else if(aa==2){
         return "Health and fitness"; 

      }else if(aa==3){
         return "Education"; 

      }else if(aa==4){
         return "Science and technology"; 

      }else if(aa==5){
         return "Money and finance"; 

      }else if(aa==6){
         return "Vehicles and transport"; 
      } 
  } 

}